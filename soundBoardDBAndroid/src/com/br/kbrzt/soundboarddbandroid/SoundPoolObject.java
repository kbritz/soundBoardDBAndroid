package com.br.kbrzt.soundboarddbandroid;

public class SoundPoolObject {

	private int soundId;
	private int image;
	private int sound;

	public SoundPoolObject() {

	}

	public int getSoundId() {
		return soundId;
	}

	public void setSoundId(int soundId) {
		this.soundId = soundId;
	}

	public int getImage() {
		return image;
	}

	public void setImage(int image) {
		this.image = image;
	}

	public int getSound() {
		return sound;
	}

	public void setSound(int sound) {
		this.sound = sound;
	}

}
